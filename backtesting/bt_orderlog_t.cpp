#include "bt_orderlog_t.h"

namespace backtesting
{

//-------------------------------------------------------------------
//-------------------------------------------------------------------

bool bt_orderlog_t::is_bids_sorted()
{
    return std::is_sorted(m_bids.begin(), m_bids.end(), bt_order_t::bids_compare);
}

//-------------------------------------------------------------------
//-------------------------------------------------------------------

bool bt_orderlog_t::is_asks_sorted()
{
    return std::is_sorted(m_asks.begin(), m_asks.end(), bt_order_t::asks_compare);
}

//-------------------------------------------------------------------
//-------------------------------------------------------------------

void bt_orderlog_t::sort_bids()
{
    std::sort(m_bids.begin(), m_bids.end(), bt_order_t::bids_compare);
}

//-------------------------------------------------------------------
//-------------------------------------------------------------------

void bt_orderlog_t::sort_asks()
{
    std::sort(m_asks.begin(), m_asks.end(), bt_order_t::asks_compare);
}

//-------------------------------------------------------------------
//-------------------------------------------------------------------

}
