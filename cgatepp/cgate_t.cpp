#include "cgate_t.h"

namespace cgatepp
{

//-------------------------------------------------------------------
//-------------------------------------------------------------------

cgate_t::cgate_t(const string& settings)
{
    CG_RESULT result;
    result = ::cg_env_open(settings.c_str());
    assert(result == CG_ERR_OK);
}

//-------------------------------------------------------------------
//-------------------------------------------------------------------

cgate_t::~cgate_t()
{
    ::cg_env_close();
}

//-------------------------------------------------------------------
//-------------------------------------------------------------------

}

